# ---------------------------------------------------------------------------
# Upload
# ---------------------------------------------------------------------------
def upload_negative(local_dir: str, suffix: str, generator: str, resolution: str,
                    dry_run: bool = False):
    repo_id = neg_repo(suffix)
    card = make_negative_card(suffix, generator, resolution)

    readme_path = Path(local_dir) / "README.md"
    readme_path.write_text(card)
    print(f"  Card written: {readme_path}")

    if dry_run:
        print(f"  [DRY RUN] Would upload {local_dir} → {repo_id}")
        return

    from huggingface_hub import HfApi
    api = HfApi()
    api.create_repo(repo_id, repo_type="dataset", exist_ok=True, private=False)
    api.upload_folder(
        folder_path=local_dir,
        repo_id=repo_id,
        repo_type="dataset",
        commit_message=f"Upload NearID-{suffix} near-identity distractors",
    )
    print(f"  Uploaded → https://huggingface.co/datasets/{repo_id}")


def upload_positives_card(dry_run: bool = False):
    card = make_positives_card()
    out = Path("scripts/dataset_cards/NearID_README.md")
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(card)
    print(f"  Positives card written: {out}")

    if dry_run:
        print(f"  [DRY RUN] Would upload card to {POSITIVES_REPO}")
        return

    from huggingface_hub import HfApi
    api = HfApi()
    api.upload_file(
        path_or_fileobj=str(out),
        path_in_repo="README.md",
        repo_id=POSITIVES_REPO,
        repo_type="dataset",
        commit_message="Update dataset card",
    )
    print(f"  Card uploaded → https://huggingface.co/datasets/{POSITIVES_REPO}")


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------
def main():
    parser = argparse.ArgumentParser(description="Upload NearID datasets to HuggingFace Hub")
    parser.add_argument("--data_root", type=str, default="./data/EncodeID",
                        help="Root directory containing EncodeID-* negative folders")
    parser.add_argument("--sources", nargs="*", default=None,
                        help="Specific sources to upload (e.g. Flux SDXL). Default: all")
    parser.add_argument("--dry_run", action="store_true",
                        help="Generate cards only, do not upload")
    parser.add_argument("--positives_only", action="store_true",
                        help="Only upload/update the positives dataset card")
    args = parser.parse_args()

    if args.positives_only:
        print("=== Positives card ===")
        upload_positives_card(dry_run=args.dry_run)
        return

    # Filter sources if specified
    sources = SOURCES
    if args.sources:
        requested = set(args.sources)
        sources = {k: v for k, v in SOURCES.items() if v[0] in requested}
        if not sources:
            print(f"No matching sources. Available: {[v[0] for v in SOURCES.values()]}")
            return

    # Upload positives card
    print("=== Positives card ===")
    upload_positives_card(dry_run=args.dry_run)

    # Upload negatives
    for local_name, (suffix, generator, resolution) in sources.items():
        local_dir = os.path.join(args.data_root, local_name)
        print(f"\n=== NearID-{suffix} ===")
        if not os.path.isdir(local_dir):
            print(f"  SKIP: {local_dir} not found")
            continue
        upload_negative(local_dir, suffix, generator, resolution, dry_run=args.dry_run)

    print("\nDone.")


if __name__ == "__main__":
    main()
